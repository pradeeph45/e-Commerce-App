import com.example.e_commerceapp.GlobalNavigation
import com.example.e_commerceapp.Model.ProductModel
import network.DebugCoil

@Composable
fun ProductItemView(modifier: Modifier = Modifier, item: ProductModel) {
    val context = LocalContext.current
    val debugImageLoader = remember {
        DebugCoil.createDebugImageLoader(context)
    }
    Card(modifier = modifier.padding(8.dp).clickable{
        GlobalNavigation.navController.navigate("product-details/"+item.id)
    }
    , shape = RoundedCornerShape(12.dp),
        colors = CardDefaults.cardColors(containerColor = MaterialTheme.colorScheme.surface),
        elevation = CardDefaults.cardElevation(8.dp)
    ) {
        Column(modifier = Modifier.padding(12.dp)) {
            AsyncImage(model = item.images.firstOrNull(),
                imageLoader = debugImageLoader,
                contentDescription = item.title,
                modifier = Modifier
                    .height(120.dp)
                    .fillMaxWidth())
            Text(item.title,
                fontWeight = FontWeight.Bold,
                maxLines = 1,
                overflow = TextOverflow.Ellipsis,
                modifier = Modifier.padding(8.dp))
            Row(
                modifier = Modifier.fillMaxWidth()
                    .padding(8.dp),
                verticalAlignment = Alignment.CenterVertically
            ) {
                Text(
                    "$"+item.price,
                    fontSize = 12.sp,
                    style = TextStyle(textDecoration = TextDecoration.LineThrough)
                )
                Spacer(modifier = Modifier.width(8.dp))
                Text(
                    "$"+item.actualPrice,
                    fontSize = 14.sp,
                    fontWeight = FontWeight.SemiBold
                )
                Spacer(modifier = Modifier.weight(1f))
                IconButton(onClick = {}) {
                    Icon(
                        imageVector = Icons.Default.ShoppingCart,
                        contentDescription = "Add to Cart"
                    )
                }
            }
        }
    }
}